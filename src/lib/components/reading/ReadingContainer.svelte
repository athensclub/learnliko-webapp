<script lang="ts" context="module">
	export type ReadingViewType = 'READ' | 'QUIZ' | 'RECAP';
</script>

<script lang="ts">
	import ReadingTextView from './ReadingTextView.svelte';
	import ReadingQuizView from './ReadingQuizView.svelte';
	import type { ReadingItem } from '$lib/types/reading';
	import type { ReadingCard } from '$gql/generated/graphql';
	import { graphqlClient } from '$lib/graphql';
	import { RECAP_READING_QUIZ, UPDATE_LESSON_PROGRESS } from '$gql/schema/mutations';
	import userSession from '$lib/stores/userSession';
	import { isMobile } from '$lib/global/breakpoints';

	/**
	 * Called when the user click 'continue' button after submitting answers.
	 */
	export let onFinish = () => {};

	export let showFinishButton = true;

	export let item: ReadingCard;

	/**
	 * function that is called to get answer index array from reading card item.
	 */
	export let answerGetter = async (
		item: ReadingCard,
		selected: number[]
	): Promise<{ answers: number[]; correct: number }> => {
		const result = await graphqlClient
			.mutation(RECAP_READING_QUIZ, {
				data: {
					quizCard: item.id,
					userAnswer: selected.map((i) => i ?? 0)
				},
				uid: $userSession.accountData?.uid!
			})
			.toPromise();

		await graphqlClient
			.mutation(UPDATE_LESSON_PROGRESS, {
				uid: $userSession.accountData?.uid!,
				data: {
					lessonId: item.fromLesson,
					quizCardId: item.id,
					quizRecapId: result.data?.readingRecapCreate.id!,
					sectionIndex: 2
				}
			})
			.toPromise();
		return {
			answers: result.data?.readingRecapCreate.answer.map((a) => a.answerIndex) ?? [],
			correct: result.data?.readingRecapCreate.totalCorrect ?? 0
		};
	};

	let selected = Array(item.questions.length).fill(null);

	let clazz = '';
	export { clazz as class };
	export let scale = 1;

	/**
	 * Set to non-null value to render this reading item as played reading container.
	 */
	export let correctAnswers: number[] | null = null;

	let currentView: ReadingViewType = 'READ';

	// onMount(() => {
	// 	resetReadingData();

	// 	//TODO: remove this and call initialize in reading.ts global file instead (when ready)
	// 	$selectedQuizChoices = Array(item.quiz.length).fill(null);
	// });
</script>

<div
	style="box-shadow: 0px 1px 10px 0px rgba(0, 0, 0, 0.25);"
	class="pointer-events-auto relative overflow-hidden bg-white {$isMobile
		? 'rounded-[5vw] p-[5vw]'
		: 'rounded-[2vw] p-[2vw]'} {clazz}"
>
	{#if currentView === 'READ'}
		<ReadingTextView {scale} {item} setView={(view) => (currentView = view)} />
	{:else if currentView === 'QUIZ'}
		<!-- bind data to persist data in container when switched to other view. -->
		<ReadingQuizView
			bind:correctAnswers
			bind:selected
			{scale}
			{showFinishButton}
			{item}
			{answerGetter}
			{onFinish}
			setView={(view) => (currentView = view)}
		/>
	{/if}
</div>
